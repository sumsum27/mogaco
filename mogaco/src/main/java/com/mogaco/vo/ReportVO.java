package com.mogaco.vo;

public class ReportVO {
	
	private int num;
	private String id;
	private String date;
	private String todayGoal;
	private String todayOutcome;
	private String text;
	private String totalIng;
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getTodayGoal() {
		return todayGoal;
	}
	public void setTodayGoal(String todayGoal) {
		this.todayGoal = todayGoal;
	}
	public String getTodayOutcome() {
		return todayOutcome;
	}
	public void setTodayOutcome(String todayOutcome) {
		this.todayOutcome = todayOutcome;
	}
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	public String getTotalIng() {
		return totalIng;
	}
	public void setTotalIng(String totalIng) {
		this.totalIng = totalIng;
	}
	
	

}
